import { writeFile } from 'node:fs/promises';
import path from 'node:path';
import { cards } from '@cometpisces/tarot-kit';
import { Converter } from 'opencc-js/t2cn';

const convert = Converter({ from: 'tw', to: 'cn' });
const strings = [];

function collectStrings(value) {
  if (typeof value === 'string') {
    strings.push(value);
  } else if (Array.isArray(value)) {
    value.forEach(collectStrings);
  } else if (value && typeof value === 'object') {
    Object.values(value).forEach(collectStrings);
  }
}

collectStrings(cards);

const characterMap = new Map();
for (const source of strings) {
  const target = convert(source);
  const sourceCharacters = [...source];
  const targetCharacters = [...target];
  if (sourceCharacters.length !== targetCharacters.length) continue;

  sourceCharacters.forEach((character, index) => {
    if (character !== targetCharacters[index] && !characterMap.has(character)) {
      characterMap.set(character, targetCharacters[index]);
    }
  });
}

// OpenCC uses context to distinguish 計畫 (plan) from 畫面 (image).
// The runtime keeps a phrase override for 計畫, so the character fallback is 畫 → 画.
characterMap.set('畫', '画');

const entries = [...characterMap.entries()].sort(([left], [right]) => left.localeCompare(right, 'zh-Hant'));
const output = `// Generated by scripts/generate-zh-hans-map.mjs.\n` +
  `// Keep phrase-sensitive replacements in domain/locale.ts.\n` +
  `export const zhHansCharacterMap: Readonly<Record<string, string>> = ${JSON.stringify(Object.fromEntries(entries), null, 2)};\n`;

await writeFile(path.join(process.cwd(), 'site/src/domain/zhHansMap.generated.ts'), output);
console.log(`Generated ${entries.length} Traditional → Simplified character mappings.`);
