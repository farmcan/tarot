export const MIAO_CHAOS_CARD_COUNT = Object.keys(MIAO_CHAOS_CARD_HOOKS).length;
export const MIAO_CHAOS_ASIDE_COUNT = MIAO_CHAOS_CARD_COUNT * 2 * MIAO_CHAOS_TEMPLATES.length;

export function getMiaoChaosAsidePool(cardName, orientation) {
  const hook = MIAO_CHAOS_CARD_HOOKS[cardName]?.[orientation];
  if (!hook) return [];
  const serious = MIAO_CHAOS_SERIOUS_POOLS.has(`${cardName}:${orientation}`);

  return MIAO_CHAOS_TEMPLATES.map((template) => ({
    patternId: serious && template.seriousPatternId
      ? template.seriousPatternId
      : template.patternId,
    text: template.render(cardName, orientation, hook, serious),
  }));
}

export function getMiaoChaosLibraryEntries() {
  return Object.entries(MIAO_CHAOS_CARD_HOOKS).flatMap(([cardName, orientations]) => (
    ['正位', '逆位'].flatMap((orientation) => (
      getMiaoChaosAsidePool(cardName, orientation).map((item) => ({
        cardName,
        orientation,
        ...item,
        hook: orientations[orientation],
      }))
    ))
  ));
}
